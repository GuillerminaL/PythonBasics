color = input("Please, enter your favourite primary color: ")
color = color.upper()

if color == "YELLOW":
    print("Great! Lemons are yellow and acid too...")
elif color == "RED":
    print("Great! Love and strawberries are red and sweet too...")
elif color == "BLUE":
    print("Great! Sky and big ideas can be deep and blue too...")
else:
    print("Well... Primary things can be confusing too!")